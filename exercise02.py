# Có 5 kiểu dữ liệu dựng sẵn: bool, sequences, sets, characters, literals
# Mỗi kiểu lấy 2 ví dụ và in ra màn hình

# bool
x = True
y = False
print(x)
print(y)

# sequences
color = ['Blue', 'Green']
size = ['S', 'M', 'X']
print(color)
print(size)

# sets
set_01 = {1, 2, 3, 4}
set_02 = set(('apple', 'banana', 'cherry'))
print(set_01)
print(set_02)

# characters
char_01 = 'A'
char_02 = 'B'
print(char_01)
print(char_02)

# literals
number_i = 123
str_ = 'Hello Ha'
print(number_i)
print(str_)